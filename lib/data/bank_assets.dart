  static List<String> banksForCountry(String countryId) {
    for (final country in supportedCountries) {
      if (country.id == countryId) {
        return country.bankIds;
      }
    }

    return supportedBanks;
  }

  static BankInfo? importedBankForId(String bankId) {
    for (final bank in importedBanks) {
      if (bank.id == bankId) {
        return bank;
      }
    }

    return null;
  }
